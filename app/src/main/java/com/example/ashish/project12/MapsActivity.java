        mMap.addMarker(new MarkerOptions().position(MIET).title("Miet"));
        mMap.moveCamera(CameraUpdateFactory.newLatLngZoom(MIET, 15));

        String str_origin = "origin=" + MIET.latitude + "," + MIET.longitude;
        String str_dest = "destination=" + AIRPORT.latitude + "," + AIRPORT.longitude;
        String sensor = "sensor=false";
        String parameters = str_origin + "&" + str_dest + "&" + sensor;
        String output = "json";
        String url = "https://maps.googleapis.com/maps/api/directions/" + output + "?" + parameters;

        Log.d("onMapClick", url.toString());
        FetchUrl FetchUrl = new FetchUrl();
        FetchUrl.execute(url);
        mMap.moveCamera(CameraUpdateFactory.newLatLng(MIET));
        mMap.animateCamera(CameraUpdateFactory.zoomTo(12));

