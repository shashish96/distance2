        Location airport_location = new Location("Airport");
        airport_location.setLatitude(AIRPORT.latitude);
        airport_location.setLongitude(AIRPORT.longitude);

        dist = (miet_location.distanceTo(airport_location))/ 1000;

        AlertDialog alertDialog = new AlertDialog.Builder(MapsActivity.this).create();
        alertDialog.setTitle("Info");
        alertDialog.setMessage("Distance between these two location is  "+dist +" kms");
